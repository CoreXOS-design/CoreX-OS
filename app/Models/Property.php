     * ownership" tasks per imported listing floods the Tasks board (it grew an
     * 18k-row backlog on staging and OOM'd the page). Genuine new mandates
     * (manual capture, promoteToStock) leave this false and still get their
     * chase tasks. Declared (not dynamic) so it survives PHP 8.2 strictness.
     */
    public bool $skipNewListingAutomation = false;

    /**
     * Off-market / terminal listing statuses — the single source of truth for
     * "this listing is NOT live on the market". Everything else (for_sale, incl.
     * Reduced Price / Pending sub-labels, under_offer, on_show, on_auction,
     * to_let, the legacy 'active', …) is considered ON MARKET. Use
     * Property::OFF_MARKET_STATUSES / scopeOnMarket() everywhere instead of
     * re-listing these literals — see BUILD_STANDARD §6 (fix the class).
     *
     * NOTE: distinct from scopeTransactionLive()'s mandate-liveness check, which
     * is about mandate expiry, not on-market display.
     */
    public const OFF_MARKET_STATUSES = [
        'sold', 'transferred', 'withdrawn', 'expired',
        'cancelled', 'let_out', 'draft', 'archived', 'unavailable',
    ];

    /**
     * On-market listings = base status NOT in OFF_MARKET_STATUSES. This is the
     * canonical definition of "active"/live stock for dashboards and filters.
     */
    public function scopeOnMarket($query)
    {
        return $query->whereNotIn('status', self::OFF_MARKET_STATUSES);
    }

    /**
     * Instance mirror of scopeOnMarket() — true when this property is live on
     * the market. Same single source of truth (OFF_MARKET_STATUSES) so the
     * row-level check can never drift from the query scope. Used by the MIC
     * stock matcher: only on-market stock may carry an "IN STOCK" badge / be
     * suppressed from the prospectable pool (BUILD_STANDARD §6 — fix the class).
     */
    public function isOnMarket(): bool
    {
        return ! in_array((string) $this->status, self::OFF_MARKET_STATUSES, true);
    }

    /**
     * The one value of p24_syndication_status / pp_syndication_status that means
     * "the portal no longer carries this listing". Every other value — including
     * the terminal-but-still-listed 'sold' / 'rented' — means the listing may
     * still be visible, so a delist must be attempted.
     */
    public const PORTAL_OFF_STATUS = 'deactivated';

    /**
     * P24 statuses that are terminal on the market but leave the listing ON the
     * portal. Written by PropertyObserver's status auto-sync; recognised here so
     * the delist guards and the portal-link builder agree on what "still live"
     * means. See Property24ListingMapper::removesFromPortal().
     */
    public const P24_ON_PORTAL_TERMINAL_STATUSES = ['sold', 'rented'];

    /**
     * Statuses that CLAIM the portal is not carrying the listing. If P24 is in
     * fact still showing one of these, it is a stranded public advert — the
     * harmful direction of status drift, and the class that stranded #2142.
     *
     * This is the suspect set for p24:reconcile-portal-presence. It is deliberately
     * WIDER than PORTAL_OFF_STATUS alone: 'error' and 'rejected' equally read as
     * "not advertised" to a human looking at the panel, and they equally hide a
     * listing that is publicly live. Sweeping only 'deactivated' left 189 rows
     * (159 error + 30 rejected) that nothing ever checked.
     *
     * NOT included: 'submitted'/'pending'/'active' (the 15-min SyncProperty24Activations
     * job owns those) and 'sold'/'rented' (terminal but legitimately still ON the
     * portal — see P24_ON_PORTAL_TERMINAL_STATUSES).
     */
    public const P24_CLAIMS_OFF_PORTAL_STATUSES = [self::PORTAL_OFF_STATUS, 'error', 'rejected'];

    /**
     * True when P24 may still be showing this listing. The ONLY safe basis for a
     * delist guard: we hold a portal reference and nothing has told us the
     * listing left the portal. Anything else — 'sold', 'rented', 'pending',
     * 'error', 'submitting' — is a listing we must still try to withdraw.
     *
     * Deliberately NOT gated on p24_syndication_enabled: that flag is a "should
     * CoreX sync" switch, and a listing toggled off while live is exactly the
     * case that stranded property #2142 on the portal.
     */
    public function mayBeLiveOnP24(): bool
    {
        return ! empty($this->p24_ref)
            && $this->p24_syndication_status !== self::PORTAL_OFF_STATUS;
    }

    /** Private Property mirror of mayBeLiveOnP24(). */
    public function mayBeLiveOnPp(): bool
    {
        return ! empty($this->pp_ref)
            && $this->pp_syndication_status !== self::PORTAL_OFF_STATUS;
    }

    /**
     * True when this property is still an unpublished draft. A draft is never
     * ready to be pushed to any portal/website — it must be set Active first.
     * Case-insensitive so 'Draft'/'DRAFT' are caught alongside the canonical
     * lowercase 'draft'. The single source of truth for the syndication draft
     * guard (EnforcesMarketingReadiness::enforceListingNotDraft()).
     */
    public function isDraft(): bool
    {
        return strtolower(trim((string) $this->status)) === 'draft';
    }

    /**
     * AT-262 — has this listing EVER been advertised (website or a portal)?
     *
     * "Ever", not "now": these markers are set when a listing goes public and are
     * NOT cleared on withdrawal (a delisted P24 listing keeps its ref/activated_at),
     * so a withdrawn-back-to-draft listing still answers true. A listing with none
     * of them has genuinely never been public.
     */
    public function wasEverAdvertised(): bool
    {
        if ($this->published_at !== null) {
            return true;                                                    // HFC website publish flag
        }
        if ($this->p24_activated_at !== null || $this->pp_activated_at !== null) {
            return true;                                                    // portal activation
        }
        if ($this->p24_last_submitted_at !== null || $this->pp_last_submitted_at !== null) {
            return true;                                                    // portal submission
        }
        if (filled($this->p24_ref) || filled($this->pp_ref)) {
            return true;                                                    // a portal listing was created
        }

        // Own website (property_website_syndication) — ever enabled / submitted /
        // activated, INCLUDING rows soft-deleted when the site was later disabled.
        // DB::table sees soft-deleted rows, so this reads true history.
        return \Illuminate\Support\Facades\DB::table('property_website_syndication')
            ->where('property_id', $this->id)
            ->where(function ($q) {
                $q->where('enabled', true)
                  ->orWhereNotNull('activated_at')
                  ->orWhereNotNull('last_submitted_at');
            })
            ->exists();
    }

    /**
     * AT-262 (Johan's gate) — "Change listing type" is available ONLY for a draft
     * that has NEVER been advertised. It is the "I loaded a rental that should have
     * been a sale" fix, nothing more. Any advertised/active listing uses Duplicate
     * instead, so its live history is never archived out from under the portals.
     */
    public function canChangeType(): bool
    {
        return $this->isDraft() && ! $this->wasEverAdvertised();
    }

    /**
     * Whether this property type is a habitable dwelling that is normally
     * listed with bedroom/bathroom counts. Land, farms, commercial and
     * industrial stock are not — so readiness/completeness gates must not
     * demand Beds/Baths for them. Classification mirrors the same
     * normalised-token matching used by the P24 type resolver
     * (Property24ListingMapper::resolvePropertyTypeId) so the two never
     * disagree on what counts as "land/commercial".
     */
    public function requiresBedsBaths(): bool
    {
        $type = (string) $this->property_type;
        if ($type === '') {
            // Unknown type: assume a dwelling so we never silently drop the
            // Beds/Baths gate for a normal residential listing.
            return true;
        }

        // Normalise: lowercase, non-alphanum → single space, collapse, trim,
        // then pad so whole-token matching ("land" not "highland") works.
        $norm = trim(preg_replace('/\s+/', ' ', preg_replace('/[^a-z0-9]+/i', ' ', strtolower($type))));
        $padded = " {$norm} ";
        $contains = static function (string ...$needles) use ($padded): bool {
            foreach ($needles as $n) {
                if (str_contains($padded, " {$n} ")) {
                    return true;
                }
            }
            return false;
        };

        // Land / farm / commercial / industrial → no Beds/Baths expected.
        if ($contains('industrial')) return false;
        if ($contains('commercial', 'office', 'retail', 'hospitality')) return false;
        if ($contains('farm', 'smallholding', 'small holding', 'agricultural')) return false;
        if ($contains('vacant land', 'land', 'plot', 'stand', 'erf')) return false;

        return true;
    }

