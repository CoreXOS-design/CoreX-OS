        $agent   = $this->agent;
        // Co-listing agent (pp_second_agent_id) — drives the dual-agent ad layouts
        // and the builder's Agent 2 fields. Null/empty keys when single-agent.
        $agent2  = ($this->pp_second_agent_id && (int) $this->pp_second_agent_id !== (int) $this->agent_id)
            ? $this->secondAgent
            : null;
        $branch  = $this->branch;
        $agency  = $this->agency;

        $logoPath = $branch?->logo_path ?: $agency?->logo_path;
        $logoUrl  = $logoPath ? self::adSafeImageUrl(asset('storage/' . $logoPath)) : null;

        $beds    = $this->beds;
        $baths   = $this->baths;
        $garages = $this->garages;
        $size    = $this->size_m2 ? number_format($this->size_m2) . ' M²' : null;

        // Status badge — honest label derived from the listing, never fabricated.
        // NOTE: 'pending' is NOT "under offer". Under the two-tier model Pending is
        // a SUB-LABEL on a For-Sale base (For Sale + Pending banner), so a pending
        // listing is still actively FOR SALE on the marketing card. Only a genuine
        // 'under_offer' base status reads "UNDER OFFER".
        // Read through normalizedStatus(): the raw column is mixed-case (P24 writes
        // 'Sold'), and a case-sensitive compare here put "FOR SALE" on the ad card
        // of 60 SOLD properties.
        $adStatus = $this->normalizedStatus();

        $statusBadge = match (true) {
            in_array($adStatus, ['sold', 'transferred'], true) => 'SOLD',
            // The rental equivalent of SOLD. Without this a tenanted property
            // generates an ad advertising it "TO LET".
            in_array($adStatus, ['rented', 'let_out'], true)   => strtoupper(str_replace('_', ' ', $adStatus)),
            $adStatus === 'under_offer'                        => 'UNDER OFFER',
            $this->isRental()                                  => 'TO LET',
            default                                            => 'FOR SALE',
        };

        return [
            'image_1'           => $img(0),
            'image_2'           => $img(1),
            'image_3'           => $img(2),
            'image_4'           => $img(3),
            'image_5'           => $img(4),
            'price'             => $this->formattedPrice(),
            'title'             => strtoupper((string) $this->title),
            'suburb'            => strtoupper((string) $this->suburb) . ($this->city ? ', ' . strtoupper((string) $this->city) : ''),
            'property_type'     => strtoupper(str_replace('_', ' ', (string) $this->property_type)),
            'features'          => trim(($beds ? $beds . ' Bed' : '') . ($baths ? ' · ' . $baths . ' Bath' : '') . ($garages ? ' · ' . $garages . ' Garage' : ''), ' · '),
            // Full amenity list — the Ad Builder "Features" element lets the user
            // pick which of these to display. Deduped, blanks dropped.
            'features_list'     => array_values(array_unique(array_filter(array_map(
                fn ($f) => trim((string) $f),
                (array) ($this->features_json ?? []),
            )))),
            'beds'              => (string) ($beds ?? ''),
            'baths'             => (string) ($baths ?? ''),
            'garages'           => (string) ($garages ?? ''),
            'size_m2'           => $size,
            'reference'         => $this->external_id ?: ('REF ' . $this->id),
            'address'           => $this->address ?: null,
            'status_badge'      => $statusBadge,
            'agent_name'        => strtoupper((string) ($agent?->name ?? '')),
            'agent_email'       => $agent?->email ?? '',
            // User has no `mobile` column — the mobile number lives in `cell`,
            // with `phone` as the landline fallback. (Was `$agent?->mobile` which
            // was always null, so the cell number never reached the ad.)
            'agent_phone'       => $agent?->cell ?: $agent?->phone ?: '',
            'agent_designation' => $agent?->designation ?? 'Property Practitioner',
            // User has no `avatar_url` column — the photo URL comes from
            // profilePhotoUrl() (user_documents → legacy agent_photo_path).
            'agent_avatar'      => self::adSafeImageUrl($agent?->profilePhotoUrl()),

            // Agent 2 — the co-listing agent (empty when the listing is single-agent).
            // Powers the dual-agent prebuilt layouts + the builder's Agent 2 fields.
            'agent_2_name'        => strtoupper((string) ($agent2?->name ?? '')),
            'agent_2_email'       => $agent2?->email ?? '',
            'agent_2_phone'       => $agent2 ? ($agent2->cell ?: $agent2->phone ?: '') : '',
            'agent_2_designation' => $agent2 ? ($agent2->designation ?: 'Property Practitioner') : '',
            'agent_2_avatar'      => self::adSafeImageUrl($agent2?->profilePhotoUrl()),
            'agent_2_initial'     => $agent2 ? strtoupper(mb_substr((string) $agent2->name, 0, 1)) : '',

            'agency_name'       => $agency?->name ?? '',
            'website'           => $agency?->website_url ?: '',
            'logo'              => $logoUrl,
            'watermark'         => strtoupper((string) ($agency?->name ?? '')),
        ];
    }

