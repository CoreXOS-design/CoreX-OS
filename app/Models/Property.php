    /**
     * Build the best human-readable address from available fields.
     * Priority: structured parts (unit_number, complex_name, street_*)
     *           ↳ legacy `address` column only when NO structured parts
     *             produced anything
     *           ↳ title as last resort.
     *
     * Build 7 fix — the legacy `address` column on many older rows is a
     * stale pre-concatenation of complex_name + unit_number (e.g.
     * property 909: address="Brock Manor, 17", complex_name="Brock Manor",
     * unit_number="17"). The pre-fix elseif chain appended the legacy
     * `address` whenever street_* was missing, re-adding content the
     * structured branch already supplied. The new chain only falls
     * through to `address` when NO structured part landed in $parts.
     * Adjacent-duplicate guard at the bottom is belt-and-braces for any
     * other overlap pattern (case-insensitive, trimmed).
     */
    public function buildDisplayAddress(): string
    {
        $parts = [];

        if (!empty($this->unit_number)) {
            $parts[] = 'Unit ' . $this->unit_number;
        } elseif (!empty($this->unit_section_block)) {
            // Blank-unit fallback (old-import safety): show the section/block, then
            // floor, so two units in the same complex are less ambiguous when the
            // unit number was never captured. Real stock carries unit_number.
            $parts[] = $this->unit_section_block;
        } elseif (!empty($this->floor_number)) {
            $parts[] = 'Floor ' . $this->floor_number;
        }
        if (!empty($this->complex_name)) {
            $parts[] = $this->complex_name;
        }

        $usedStructuredStreet = false;
        // AT-266 — guarded street line: never prepend the number a second time
        // when street_name already opens with it ("21" + "21 Crown Road" is
        // "21 Crown Road", not "21 21 Crown Road"). Shared with
        // composeAddressFromParts() so every Property address composer agrees.
        if (!empty($this->street_name)) {
            $parts[] = $this->composeStreetLine();
            $usedStructuredStreet = true;
        }

        // Legacy `address` fallback fires ONLY when nothing structural
        // landed in $parts. Unit/complex/street are all considered
        // structural — once any one of them populated $parts, the
        // legacy column would just re-add overlapping content.
        if (empty($parts) && !empty($this->address)) {
            $parts[] = $this->address;
        }

        if (!empty($this->suburb)) {
            $parts[] = $this->suburb;
        }

        if (!empty($this->city) && strtolower($this->city) !== strtolower($this->suburb ?? '')) {
            $parts[] = $this->city;
        }

        if (empty($parts)) {
            return $this->title ?? 'Unknown Property';
        }

        // Belt-and-braces — collapse adjacent duplicates after trimming
        // + case-folding. Guards against any future pattern that lands
        // two equivalent parts side-by-side.
        $cleaned = [];
        foreach ($parts as $piece) {
            $piece = trim((string) $piece);
            if ($piece === '') continue;
            if (!empty($cleaned) && mb_strtolower(end($cleaned)) === mb_strtolower($piece)) {
                continue;
            }
            $cleaned[] = $piece;
        }

        return implode(', ', $cleaned);
    }

    /**
     * AT-266 — the ONE truth for `properties.address`.
     *
     * `address` is a DERIVED display string, composed from the structured address
     * columns. It is not an independent fact and must never be edited on its own.
     *
     * Before this, `address` and the structured columns were two free-floating
     * copies of the same fact with nothing reconciling them: the P24 import wrote
     * both consistently, the edit modal then let an agent rewrite the structured
     * columns while `address` sat frozen as a hidden passthrough, and the two
     * drifted. On 74 live properties they disagree; on 17 of those the disagreement
     * was bad enough to pitch a seller an address their own agent never typed.
     *
     * The direction — structured → address — is the one every existing convention
     * already points at: PropertyObserver::saving() derives title_type from
     * property_type the same way; P24ListingsCsvParser derives address from the
     * street columns; buildDisplayAddress() treats the structured parts as primary
     * and the legacy `address` as a last-resort fallback; and the Internal Address
     * modal already exposes the structured fields as the editable ones.
     *
     * Scheme identity leads (a sectional unit is "Unit 6, Arista, 40 Bulwer
     * Street"), then the street. Deliberately NO suburb/city — those live in their
     * own columns, `address` has always been the property line alone, and adding
     * them would silently rewrite what 4,679 correct rows say.
     *
     * Returns '' when no structured part is set — the caller keeps the existing
     * value rather than blanking a row we know nothing better about.
     */
    public function composeAddressFromParts(): string
    {
        $unit = trim((string) ($this->unit_number ?? '')) !== ''
            ? 'Unit ' . trim((string) $this->unit_number)
            : (trim((string) ($this->unit_section_block ?? '')) !== ''
                ? trim((string) $this->unit_section_block)
                : null);

        $street = $this->composeStreetLine();

        $parts = array_filter([
            $unit,
            trim((string) ($this->complex_name ?? '')) !== '' ? trim((string) $this->complex_name) : null,
            $street !== '' ? $street : null,
        ]);

        // Collapse adjacent duplicates (a complex named in both the complex column
        // and the street line reads once, not twice).
        $cleaned = [];
        foreach ($parts as $piece) {
            if (!empty($cleaned) && mb_strtolower(end($cleaned)) === mb_strtolower($piece)) {
                continue;
            }
            $cleaned[] = $piece;
        }

        return implode(', ', $cleaned);
    }

    /**
     * AT-266 — the ONE guarded street line, shared by every Property address
     * composer (buildDisplayAddress + composeAddressFromParts, which in turn
     * feeds the derived `address` column via PropertyObserver::saving()).
     *
     * "street_number street_name" — but NEVER prepend the number when the name
     * already opens with it as a whole token. Machine-written parts (P24 import,
     * parsers) sometimes carry the number in BOTH columns; the naive concat then
     * produced "21 21 Crown Road" / "16 16 Lilliecrona Boulevard", and because
     * `address` is derived from this composer that junk reached the seller-outreach
     * merge field. Mirrors OutreachAddress::composeStreetFromColumns() exactly so
     * the two never diverge again. Clean rows are unchanged — the guard only fires
     * when the name already starts with the number.
     */
    public function composeStreetLine(): string
    {
        $number = trim((string) ($this->street_number ?? ''));
        $name   = trim((string) ($this->street_name ?? ''));

        if ($name === '') {
            return $number;
        }
        if ($number === '' || preg_match('/^' . preg_quote($number, '/') . '(?![0-9A-Za-z])/u', $name)) {
            return $name;
        }

        return $number . ' ' . $name;
    }

    // ── Scopes ──

    /**
     * Scope: search across all address-related fields.
     */
    public function scopeSearchAddress($query, string $term)
    {
        // Multi-term AND across all address fields: EACH whitespace-separated token
        // must match SOME field, so "23 sherwood" / "unit 23 sherwood" narrows to
        // the right unit (23 hits unit_number/street_number, sherwood hits
        // complex_name) instead of returning everything matching either word. A
        // single token degrades to the original OR-across-fields behaviour. This is
        // the ONE canonical property search — every picker calls it (fix-the-class).
        $tokens = preg_split('/\s+/', trim($term), -1, PREG_SPLIT_NO_EMPTY);
        if (empty($tokens)) {
            return $query;
        }

        foreach ($tokens as $token) {
            $query->where(function ($q) use ($token) {
                $like = "%{$token}%";
                $q->where('address', 'like', $like)
                  ->orWhere('street_name', 'like', $like)
                  ->orWhere('street_number', 'like', $like)
                  ->orWhere('title', 'like', $like)
                  ->orWhere('suburb', 'like', $like)
                  ->orWhere('city', 'like', $like)
                  ->orWhere('complex_name', 'like', $like)
                  ->orWhere('unit_number', 'like', $like)
                  ->orWhere('unit_section_block', 'like', $like)
                  ->orWhere('property_number', 'like', $like)
                  ->orWhere('p24_ref', 'like', $like);
            });
        }

        return $query;
    }

    /**
     * The listing is CONCLUDED — the deal is done and it must never be advertised
     * as available again. Sale side: sold/transferred. Rental side: rented/let_out
     * (the rental equivalent of "sold", and the pair everyone forgets).
     */
    private const CONCLUDED_STATUSES = ['sold', 'transferred', 'rented', 'let_out'];

    /** Off-market, but not concluded — withdrawn, expired, never published, etc. */
    private const INACTIVE_STATUSES = ['withdrawn', 'cancelled', 'expired', 'unavailable', 'archived', 'draft'];

    /**
     * THE single source of truth for reading `status`. ALWAYS compare against this,
     * never against the raw attribute.
     *
     * `properties.status` is NOT normalised on write and is genuinely mixed-case in
     * production: the wizard writes lowercase ('active', 'withdrawn'), while the P24
     * sync writes capitalised labels ('Active' — 444 rows, 'Sold' — 60, 'Rented').
     * A case-sensitive `in_array($status, ['sold', …], true)` therefore MISSES every
     * 'Sold' row and falls through to the default arm — which is how 60 SOLD
     * properties came to be badged "For Sale" on the pickers and "FOR SALE" on
     * generated ad cards. Same bug-class as [[isRental]] and the same cure: tolerate
     * the vocabulary in one place so no caller has to know it varies.
     */
    public function normalizedStatus(): string
    {
        return strtolower(trim((string) $this->status));
    }

    /** A concluded listing (sold / transferred / rented / let out). */
    public function isConcluded(): bool
    {
        return in_array($this->normalizedStatus(), self::CONCLUDED_STATUSES, true);
    }

    /**
     * Honest, picker-facing status label (For Sale / Sold / Under Offer / To Let /
     * Withdrawn / Expired …). Distinct from the marketing-card badge — pickers want
     * the real lifecycle state so an agent can tell a live listing from a dead one.
     */
    public function statusBadge(): string
    {
        $status = $this->normalizedStatus();

        return match (true) {
            in_array($status, ['sold', 'transferred'], true) => 'Sold',
            $status === 'under_offer'                        => 'Under Offer',
            // A concluded rental reads "Rented" / "Let Out" — never "To Let", which
            // would advertise a tenanted property as available.
            in_array($status, ['rented', 'let_out'], true)   => ucwords(str_replace('_', ' ', $status)),
            in_array($status, self::INACTIVE_STATUSES, true) => ucwords(str_replace('_', ' ', $status)),
            $this->isRental()                                => 'To Let',
            default => 'For Sale',
        };
    }

    /**
     * THE canonical property-picker result row. Every search/typeahead surface maps
     * its results through this so the shape (full address label + listing agent +
     * status) can never drift. $extra merges surface-specific fields (price, ref,
     * seller, fees …) without re-implementing the core. Eager-load `agent` to avoid
     * N+1. Fix-the-class companion to scopeSearchAddress().
