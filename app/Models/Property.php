    /**
     * Normalise a stored image URL to one that actually loads in the browser
     * (and that Meta can fetch when publishing). Our public storage is always
     * served at `/storage/...`; stored URLs can carry a stale/localhost host or
     * the wrong scheme (from seeding/import), which then fail on staging/prod.
     * So we re-home any of-our-storage URL onto the CURRENT app host, and leave
     * genuinely external URLs untouched.
     */
    public static function publicImageUrl(?string $u): ?string
    {
        if ($u === null) {
            return null;
        }
        $u = trim($u);
        if ($u === '') {
            return null;
        }

        // Host-relative → absolute on the current host.
        if (str_starts_with($u, '/')) {
            return asset(ltrim($u, '/'));
        }

        // CONSERVATIVE: only re-home URLs baked with a local/dev host (common after
        // copying a dev DB to staging/prod). Every other working absolute URL —
        // staging, prod, a CDN, or an external source — is left exactly as stored,
        // so we never rewrite a URL that already loads.
        $host = strtolower((string) parse_url($u, PHP_URL_HOST));
        if (in_array($host, ['localhost', '127.0.0.1', '0.0.0.0', '::1'], true)) {
            $path = parse_url($u, PHP_URL_PATH) ?: '';
            return $path !== '' ? asset(ltrim($path, '/')) : $u;
        }

        return $u;
    }

    /**
     * Image URL safe for the Ad Manager's client-side html2canvas capture.
     *
     * The exported PNG is rasterised from a canvas, and a canvas can only read
     * pixels from SAME-ORIGIN images. Our images live under /storage/, but they
     * are stored (and asset()-generated) as absolute URLs pinned to APP_URL
     * (corexos.co.za). A user who reaches the app on another live host
     * (corex.hfcoastal.co.za, www.corexos.co.za) therefore loads those images
     * cross-origin — they display in the builder but /storage/ sends no CORS
     * headers, so html2canvas silently drops them from the download (property
     * photo missing while text/shapes remain).
     *
     * Emitting a HOST-RELATIVE path makes the browser load the image from the
     * current origin whichever of our domains the user is on → same-origin →
     * html2canvas reads it. Genuinely external images (not under /storage/) are
     * left absolute (nothing we can re-home).
     *
     * BUT re-homing is only safe when THIS host actually serves the file. On a
     * working-copy environment (e.g. Staging referencing images whose files live
     * on the live host) a host-relative "/storage/…" would 404 — the photos
     * vanish from every ad template while the brochure (absolute URL) still
     * loads. So we re-home ONLY when the file physically exists under this host's
     * public/storage; otherwise we keep the absolute URL so the image still
     * displays (accepting that a cross-origin html2canvas export may drop it —
     * showing the photo beats hiding it, and real exports run on the host that
     * owns the files, where re-homing still applies).
     */
    public static function adSafeImageUrl(?string $u): ?string
    {
        $u = self::publicImageUrl($u);
        if ($u === null) {
            return null;
        }

        $path = parse_url($u, PHP_URL_PATH) ?: '';
        $pos  = strpos($path, '/storage/');
        if ($pos !== false) {
            $rel = substr($path, $pos); // "/storage/…"
            // Fast path: the file IS on this host → serve it host-relative
            // (same-origin, direct from the web server, html2canvas-safe).
            if (is_file(public_path(ltrim($rel, '/')))) {
                return $rel;
            }
            // The file lives on ANOTHER of our hosts (e.g. Staging referencing
            // live-hosted images). A host-relative path would 404 and an absolute
            // cross-origin URL exports BLANK from html2canvas. Route it through
            // the same-origin ad-media proxy so it both displays AND captures.
            // `absolute:false` → root-relative, so it's same-origin on any host.
            return route('corex.properties.ad-media', ['u' => $u], false);
        }

        return $u;
    }

