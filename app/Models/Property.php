    /**
     * P24 slug-composed direct listing URL. Returns null unless we have an
     * activated p24_ref. Sandbox vs production picked from p24_syndication_status
     * to stay consistent with the legacy inline JS — in-flight states (submitted,
     * pending) don't yet point at a live page on P24. 'sold' / 'rented' listings
     * DO have a live page: P24 keeps terminal-but-not-removed stock on the portal.
     */
    private function buildP24Url(): ?string
    {
        $onPortal = array_merge(['active'], self::P24_ON_PORTAL_TERMINAL_STATUSES);

        if (empty($this->p24_ref) || ! in_array((string) $this->p24_syndication_status, $onPortal, true)) {
            return null;
        }
        $slugify = static function (?string $s): string {
            $s = (string) ($s ?? '');
            $s = strtolower(preg_replace('/[^a-z0-9]+/i', '-', $s) ?? '');
            return trim($s, '-') ?: 'property';
        };
        $section = $this->isRental() ? 'to-rent' : 'for-sale';
        $domain  = 'www.property24.com';
        return sprintf(
            'https://%s/%s/%s/%s/%s/%s/%s',
            $domain,
            $section,
            $slugify($this->suburb),
            $slugify($this->city),
            $slugify($this->province),
            // P24 suburb id segment — must be the P24 id, not PP's. (The
            // listing resolves by the trailing p24_ref regardless, but the
            // segment should still be correct.)
            $this->p24_suburb_id ?? '0',
            $this->p24_ref,
        );
    }

    /**
     * Compose the public Private Property listing URL. Pattern (live):
     *   https://www.privateproperty.co.za/{section}/{province}/{city}/{suburb}/{ref}
     *
     * PP doesn't return a listing URL from syndication, but it resolves a
     * listing purely by the trailing reference — the slug segments are
     * decorative SEO and PP redirects to the canonical URL regardless
     * (verified: a deliberately wrong province/city/suburb still served the
     * right listing). The old `/search?q=<ref>` hop now 404s ("This space is
     * no longer occupied"), so we build the SEO path and let the ref resolve.
     * We omit PP's region tier (kzn-south-coast) — we don't store PP's region
     * taxonomy and PP backfills it on redirect.
     *
     * Returns null unless PP has assigned this listing a reference.
     *
     * Gate on pp_ref presence, NOT on pp_syndication_status === 'active': PP
     * only issues a pp_ref once the listing is published, so the ref is the
     * durable "live on PP" signal. The status flag, by contrast, flaps —
     * submitListing() resets it to 'submitted' on every routine re-push, and the
     * activation-sync job only reconciles rows where pp_ref IS NULL, so a
     * re-pushed listing stays stuck at 'submitted' and never returns to
     * 'active'. Tying the public link to that flag made "View on PP" dead (href
     * '#', reopening the CoreX page) for every listing that had ever been
     * re-pushed. We still suppress the link once the listing is explicitly
     * taken off-market (deactivated).
     */
    private function buildPpUrl(): ?string
    {
        $offMarket = ['deactivated', 'disabled', 'archived', 'removed', 'expired'];
        if (empty($this->pp_ref) || in_array($this->pp_syndication_status, $offMarket, true)) {
            return null;
        }
        $slugify = static function (?string $s): string {
            $s = strtolower(preg_replace('/[^a-z0-9]+/i', '-', (string) ($s ?? '')) ?? '');
            return trim($s, '-') ?: 'property';
        };
        $section = $this->isRental() ? 'to-rent' : 'for-sale';
        return sprintf(
            'https://www.privateproperty.co.za/%s/%s/%s/%s/%s',
            $section,
            $slugify($this->province),
            $slugify($this->city ?? $this->town),
            $slugify($this->suburb),
            rawurlencode((string) $this->pp_ref),
        );
    }

    /**
     * Canonical, extensible list of public portal links for this listing.
     *
     * This is the SINGLE source of truth for "where can the public see this
     * listing" — consumed by the web "Open listing" CTAs and by the mobile
     * app's property Overview screen. Each entry is normalised to a fixed
     * shape so any client renders any portal — including future ones —
     * without code changes:
     *
     *   [
     *     'portal' => 'property24',     // stable machine key
     *     'label'  => 'Property24',     // human label
     *     'status' => 'live'|'not_published',
     *     'url'    => 'https://…'|null, // present only when it resolves live
     *     'ref'    => '…'|null,         // the portal's listing reference
     *   ]
     *
     * Adding a portal here makes it appear everywhere (web + mobile) at once —
     * that is the whole point. "Any other portal that comes along" plugs in
     * by adding one block below.
     *
     * @return array<int, array{portal:string,label:string,status:string,url:?string,ref:?string}>
     */
    public function portalLinks(): array
    {
        $links = [];

        // ── Company website ───────────────────────────────────────
        // Live when the listing is enabled on at least one agency website
        // (the property_website_syndication pivot — same check the mobile
        // Overview placement used). The public URL is composed by the
        // config-driven public_url accessor (never hardcoded).
        // Eager-loaded on list screens (many properties per page) so the check
        // costs one query for the page rather than one per property.
        $websiteLive = $this->relationLoaded('websiteSyndication')
            ? $this->websiteSyndication->contains(fn ($row) => (bool) $row->enabled)
            : \App\Models\PropertyWebsiteSyndication::withoutGlobalScope(\App\Models\Scopes\AgencyScope::class)
                ->where('property_id', $this->id)
                ->where('enabled', true)
                ->exists();
        $links[] = [
            'portal' => 'website',
            'label'  => 'Company Website',
            'status' => $websiteLive ? 'live' : 'not_published',
            'url'    => $websiteLive ? $this->public_url : null,
            'ref'    => $this->external_id,
        ];

        // ── Property24 ─────────────────────────────────────────────
        // The `enabled` switch is authoritative, not the status column. Turning
        // a portal off leaves `*_syndication_status` at its last value — the
        // deactivate call updates it only on success, and legacy rows never had
        // it cleared — so a disabled listing routinely still reads 'active'.
        // Trusting the status alone marked sold, fully-unsyndicated listings as
        // live. A portal is live only when the agency has it switched ON *and*
        // the portal has confirmed the listing.
        $p24Url  = $this->buildP24Url();
        $p24Live = $p24Url !== null && (bool) $this->p24_syndication_enabled;
        $links[] = [
            'portal' => 'property24',
            'label'  => 'Property24',
            'status' => $p24Live ? 'live' : 'not_published',
            'url'    => $p24Live ? $p24Url : null,
            'ref'    => $this->p24_ref,
        ];

        // ── Private Property ───────────────────────────────────────
        $ppUrl  = $this->buildPpUrl();
        $ppLive = $ppUrl !== null && (bool) $this->pp_syndication_enabled;
        $links[] = [
            'portal' => 'private_property',
            'label'  => 'Private Property',
            'status' => $ppLive ? 'live' : 'not_published',
            'url'    => $ppLive ? $ppUrl : null,
            'ref'    => $this->pp_ref,
        ];

        return $links;
    }

    /**
     * The list of gallery tags currently available on this property.
     *
     * Tags are derived from the property's `spaces_json` (preferred) or
     * the legacy beds/baths/garages columns. ONLY spaces the user has
     * actually added (count >= 1) produce tags — no hardcoded defaults.
     *
     * Used by:
     *   - Web gallery tagger (resources/views/corex/properties/show.blade.php)
     *   - Mobile API (App\Http\Controllers\Api\MobilePropertyController)
     *
     * @return string[]
     */
    public function getAvailableGalleryTags(): array
    {
        $tags = $this->derivedGalleryTags();

        // Append every tag that exists BEYOND the derived room set, case-
        // insensitively de-duped against what we already have. Two sources:
        //
        //   1. gallery_custom_tags — the explicit custom-tag registry (mobile
        //      add/remove endpoints keep it; the web sorter does NOT write it).
        //   2. category names already in use in gallery_categories_json — tags
        //      the agent created in the web gallery sorter and FILED PHOTOS
        //      under. That save path persists the names ONLY inside
        //      gallery_categories_json, never into gallery_custom_tags, so
        //      before this a custom tag with photos silently vanished from the
        //      tag library on reload (property 6060: "View", "Store room", …
        //      had photos but were gone from the list). A tag that has images
        //      filed under it MUST appear in the list — deriving the list from
        //      in-use categories makes it self-consistent regardless of which
        //      save path wrote them.
        $appendIfNew = function (?string $name) use (&$tags): void {
            if (!is_string($name)) return;
            $name = trim($name);
            if ($name === '') return;
            foreach ($tags as $t) {
                if (strcasecmp($t, $name) === 0) return;
            }
            $tags[] = $name;
        };

        foreach (($this->gallery_custom_tags ?? []) as $custom) {
            $appendIfNew($custom);
        }

        foreach (($this->gallery_categories_json['categories'] ?? []) as $cat) {
            $appendIfNew($cat['name'] ?? null);
        }

        return $tags;
    }

    /**
     * The gallery tags DERIVED from the property's rooms — `spaces_json`
     * (preferred) or the legacy beds/baths/garages columns. ONLY spaces the
     * user has actually added (count >= 1) produce tags. This is the room-based
     * baseline; custom tags are layered on top by getAvailableGalleryTags().
     * Kept separate so the reorder-save path can tell custom tags from derived
     * ones when persisting the registry.
     *
     * @return string[]
     */
    public function derivedGalleryTags(): array
    {
        $allowed = ['Bedroom','Bathroom','Kitchen','Lounge','Dining Room','Study','Patio','Garden','Pool','Flatlet','Garage'];

        // Prefer spaces_json — it's the canonical source after the
        // user has touched the Spaces editor.
        $spacesData = $this->spaces_json ?? [];
        $spacesList = $spacesData['spaces'] ?? [];
        if (empty($spacesList) && !empty($spacesData) && isset($spacesData[0]['type'])) {
            $spacesList = $spacesData; // legacy flat shape
        }

        $tags = [];

        if (!empty($spacesList)) {
            foreach ($spacesList as $sp) {
                $type  = $sp['type'] ?? '';
                $count = (int) ($sp['count'] ?? 0);
                if ($count < 1 || !in_array($type, $allowed, true)) continue;

                if ($count > 1) {
                    for ($i = 1; $i <= $count; $i++) $tags[] = $type . ' ' . $i;
                } else {
                    $tags[] = $type;
                }
            }
        } else {
            // Fallback: derive from legacy columns
            for ($i = 1; $i <= (int) ($this->beds ?? 0); $i++)  $tags[] = 'Bedroom ' . $i;
            for ($i = 1; $i <= (int) ($this->baths ?? 0); $i++) $tags[] = 'Bathroom ' . $i;
            if ((int) ($this->garages ?? 0) > 0) $tags[] = 'Garage';
        }

        return $tags;
    }

