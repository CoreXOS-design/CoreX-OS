    /**
     * P24 rejects a single submit whose images together exceed 60MB of data.
     * We pack to a budget below that hard cap, measured on the base64-encoded
     * size (the bytes actually transmitted) so the request stays under the cap
     * regardless of whether P24 counts raw or encoded bytes — with headroom for
     * the rest of the JSON payload. Only large galleries ever hit this; a set
     * that already fits is sent untouched at full quality.
     */
    private const P24_MAX_IMAGE_PAYLOAD_BYTES     = 60 * 1024 * 1024;
    private const P24_IMAGE_PAYLOAD_BUDGET_BYTES   = 57 * 1024 * 1024;

    /**
     * Map a CoreX Property to a P24 Listing payload matching the v53 API schema.
     *
     * @throws Property24ConfigurationException when the property's branch/agency
     *         has no P24 agency ID configured.
     */
    public function map(Property $property, bool $includePhotos = true): array
    {
        $agencyId       = $this->resolveP24AgencyId($property);
        $suburbId       = $this->resolveSuburbId($property);
        $propertyTypeId = $this->resolvePropertyTypeId($property->property_type);

        $listing = [
            'agencyId'          => $agencyId,
            'contactAgentIds'   => $this->resolveContactAgentIds($property, $agencyId),
            'listingType'       => $this->mapListingType($property->listing_type ?? $property->mandate_type),
            'status'            => $this->mapPropertyStatus($property),
            // P24 carries the monthly rent in `price` for rentals; effectivePrice()
            // returns rental_amount for rental stock, price for sales — one source.
            'price'             => $property->effectivePrice(),
            'isPOA'             => (bool) $property->price_on_application,
            'listingVisibility' => 'Public',
            'expiryDate'        => $property->expiry_date?->format('Y-m-d\TH:i:s')
                                   ?? now()->addYear()->format('Y-m-d\TH:i:s'),
            'description'       => $property->description ?? '',
            'descriptionHeader' => $property->headline ?? $property->title ?? '',
            'propertyInfo'      => $this->buildPropertyInfo($property, $suburbId, $propertyTypeId),
            'propertyFeatures'  => $this->buildPropertyFeatures($property),
            'youTubeVideoId'    => $this->extractYouTubeId($property->youtube_video_id),
            'matterportSpaceId' => $property->matterport_id ?: null,
        ];

        // Top-level P24 `tags[]` — GLOBAL features that map to a Tag enum member
        // AND have NO PropertyFeatures field (security/connectivity/descriptive:
        // alarm, burglar bars, electric fence/gate, 24hr, CCTV, TV Port-global,
        // sea view, ports, air-con, …). Amenities WITH a PropertyFeatures field are
        // routed there (above) — never duplicated as a loose tag.
        $tags = $this->buildTags($property);
        if (!empty($tags)) {
            $listing['tags'] = $tags;
        }

        // AT-102/AT-103 — per-room named rooms with their feature chips ONLY. The
        // room label is generated by P24 from featureType; it is never put in
        // description/tags. featureType "Other" is never sent. Global amenities live
        // in PropertyFeatures (above) or tags[] — not here.
        $featureTags = $this->buildFeatureTags($property);
        if (!empty($featureTags)) {
            $listing['featureTags'] = $featureTags;
        }

        // AT-P24 remediation (#6): a coordinate stored as the string
        // "0.00000000" is truthy in PHP, so the old `$lat && $lng` guard let 0,0
        // through and P24 rejected it ("Latitude and Longitude can not both be
        // 0."). Compare numerically so any zero/blank coordinate is skipped.
        if ((float) $property->latitude != 0.0 && (float) $property->longitude != 0.0) {
            $listing['propertyInfo']['geographicLocation'] = [
                'latitude'  => (float) $property->latitude,
                'longitude' => (float) $property->longitude,
            ];
        }

        if ($property->p24_ref) {
            $listing['listingNumber'] = (int) $property->p24_ref;
            // Only override to Active if property is still on market
            if ($listing['status'] === 'NewListing') {
                $listing['status'] = 'Active';
            }
        }

        // Complex info — always sent (P24 needs the full address regardless of
        // the public-display flag).
        if ($property->complex_name || $property->unit_number) {
            $listing['complexInfo'] = [
                'complexName' => $property->complex_name ?? null,
                'unitNumber'  => $property->unit_number ?? null,
            ];
        }

        // Rental info
        if ($this->mapListingType($property->listing_type ?? $property->mandate_type) === 'Rental') {
            $rentalInfo = ['leasePeriod' => $property->lease_period ?? '12 Months'];
            // Rate cadence — CoreX stores rental_price_type ("per week"/"per
            // day"/…). Without emitting it, every weekly/daily/short-term rental
            // publishes at P24's default monthly cadence. Enum: Month/Week/Day/
            // Year/SquareMetre.
            if ($rentalRate = $this->mapRentalRate($property->rental_price_type ?? null)) {
                $rentalInfo['rentalRate'] = $rentalRate;
            }
            if ($property->deposit_amount) {
                $rentalInfo['depositRequirementsComments'] = 'Deposit: R ' . number_format((float) $property->deposit_amount, 0, '.', ' ');
            }
            $listing['rentalInfo'] = $rentalInfo;
        }

        // Commercial info — match on substring so the canonical CoreX labels
        // ("Commercial Property", "Industrial Property") fire, not just the bare
        // words. The old exact in_array() gate missed every live commercial
        // listing (all typed "Commercial Property"/"Industrial Property"), so
        // commercialInfo never emitted. Aligned with resolvePropertyTypeId().
        $ptLower = strtolower($property->property_type ?? '');
        if (str_contains($ptLower, 'commercial') || str_contains($ptLower, 'industrial')
            || str_contains($ptLower, 'office') || str_contains($ptLower, 'retail')) {
            $commercial = [];
            if ($property->gross_price) $commercial['grossPrice'] = (float) $property->gross_price;
            if ($property->net_price) $commercial['netPrice'] = (float) $property->net_price;
            if ($property->lease_start_date) $commercial['availabilityDate'] = $property->lease_start_date->format('Y-m-d');

            // AT-P24 remediation (#9) — previously-UNROUTED commercial amenities
            // with a VERBATIM PropertyFeatures/CommercialInfo home in the v53
            // schema (CommercialInfo.greenBuilding / multiTenanted /
            // hasNaturalLight). Sourced from the property-screen global features.
            $gf  = array_map('strtolower', $this->globalFeatures($property));
            $has = fn (string $n) => in_array(strtolower($n), $gf, true);
            if ($has('Green Building')) $commercial['greenBuilding'] = true;
            if ($has('Multi Tenanted')) $commercial['multiTenanted'] = true;
            if ($has('Natural Light')) $commercial['hasNaturalLight'] = true;

            if (!empty($commercial)) $listing['commercialInfo'] = $commercial;
        }

        // Occupation date
        if ($property->lease_start_date) {
            $listing['occupationDate'] = $property->lease_start_date->format('Y-m-d\TH:i:s');
        }

        // Showdays
        $showdays = $this->buildShowdays($property);
        if (!empty($showdays)) {
            $listing['showDays'] = $showdays;
        }

        if ($includePhotos) {
            // AT-P24: only (re)upload photos when the gallery actually changed
            // since the last successful sync. P24 keeps the existing photos when
            // `photos` is null (swagger: "set photos to null" when unchanged),
            // so a routine refresh skips the heavy base64 upload. A NEW listing
            // (no p24_ref) or one with no recorded signature always sends its
            // photos.
            $imagesChanged = empty($property->p24_ref)
                || $property->p24_image_signature === null
                || $property->p24_image_signature !== $property->p24ImageSignature();

            if ($imagesChanged) {
                $photos = $this->buildPhotos($property);
                if (!empty($photos)) {
                    $listing['photos'] = $photos;
                }
            } else {
                // Unchanged → tell P24 explicitly to keep the existing photos.
                $listing['photos'] = null;
            }
        }

        return $listing;
    }

    /**
     * Map CoreX rental_price_type ("per month"/"per week"/…) to P24's RentalRate
     * enum (Month/Week/Day/Year/SquareMetre). Returns null for an unknown/blank
     * cadence so P24 applies its own default (Month) rather than us asserting one.
     */
    private function mapRentalRate(?string $rentalPriceType): ?string
    {
        return match (strtolower(trim((string) $rentalPriceType))) {
            'per month', 'per_month', 'monthly'                          => 'Month',
            'per week', 'per_week', 'weekly'                             => 'Week',
            'per day', 'per_day', 'daily'                                => 'Day',
            'per year', 'per_year', 'yearly', 'annually'                 => 'Year',
            'per sqm', 'per_sqm', 'persqm', 'per m2', 'per_m2',
            'persquaremetre', 'per square metre', 'per square meter'     => 'SquareMetre',
            default                                                      => null,
        };
    }

    private function buildPropertyInfo(Property $property, ?int $suburbId, ?int $propertyTypeId): array
    {
        // P24 "hide address" affects ONLY public display, never the data we send.
        // P24's verification team REQUIRES the full address always (to vet
        // legitimacy, link/group properties, confirm the address is real), so the
        // street number/name, stand number, complex/unit and GPS are ALWAYS sent.
        // p24_hide_address=1 merely sets showLocation=false so P24 does not show
        // the address publicly. Independent of the PP pp_hide_* flags.
        $info = [
            'suburbId'        => $suburbId,
            'propertyTypeId'  => $propertyTypeId,
            'streetNumber'    => $property->street_number ?? '',
            'streetName'      => $property->street_name ?? $this->parseStreetName($property->address),
            'sourceReference' => 'CoreX-' . $property->id,
            // latitude/longitude are decimal:7 casts → Laravel returns them as
            // strings ("0.0000000"), which are truthy, so the old `$lat && $lng`
            // guard emitted showLocation=true for never-geocoded listings (P24
            // could then surface the street address the agent never published).
            // Compare numerically — same fix already applied to the
            // geographicLocation guard above.
            'showLocation'    => $property->p24_hide_address
                ? false
                : ((float) $property->latitude != 0.0 && (float) $property->longitude != 0.0),
        ];

        if ($property->stand_number) $info['standNumber'] = $property->stand_number;
        if ($property->erf_size_m2) $info['erf'] = ['areaUnit' => 'SquareMetres', 'size' => (float) $property->erf_size_m2];
        if ($property->size_m2) $info['floorArea'] = ['areaUnit' => 'SquareMetres', 'size' => (float) $property->size_m2];
        if ($property->floor_number) $info['floorNumber'] = (int) $property->floor_number;
        if ($property->rates_taxes) $info['municipalRatesAndTaxes'] = ['amount' => (float) $property->rates_taxes, 'unit' => 'TotalPrice'];
        if ($property->levy) $info['monthlyLevy'] = ['amount' => (float) $property->levy, 'unit' => 'TotalPrice'];
        if ($property->special_levy) $info['specialLevy'] = (float) $property->special_levy;

        return $info;
    }

    private function buildPropertyFeatures(Property $property): array
    {
        // AT-103/AT-102 — most property-level booleans (hasFeature) source from
        // GLOBAL-ONLY features (the property feature screen = spaces_json['features']),
        // NOT the flat features_json which also carries per-room unit features. A
        // feature that exists only on a room must not flip a property-level boolean.
        $feats = $this->globalFeatures($property);
        $spacesData = $property->spaces_json ?? [];
        $spacesList = $spacesData['spaces'] ?? (isset($spacesData[0]) ? $spacesData : []);
        $hasFeature = fn(string ...$names) => !empty(array_intersect(array_map('strtolower', $feats), array_map('strtolower', $names)));
        $countSpaces = fn(string $type) => collect($spacesList)->where('type', $type)->sum(fn($s) => (float) ($s['count'] ?? 1));

        // AT-146 (property 6049) — DEDICATED-SPACE amenities (parking, kitchen
        // fittings, outside areas) are entered on their own space, never on the
        // global feature screen (parking/kitchen are not one of the 4 global
        // categories), so globalFeatures() ALWAYS strips them and their structured
        // PropertyFeatures booleans (parking.visitorsParking, kitchens.dishwasher,
        // outsideArea.balcony, …) could never be true for any property with
        // structured spaces. These booleans answer "does the property have X
        // anywhere", so they source from the FULL feature set (flat features_json
        // ∪ every space/unit). globalFeatures() is unchanged for tags[] (phantom-
        // room-sensitive) and for the genuinely-global booleans above.
        $allFeats = $this->allFeatures($property);
        $hasAny = fn(string ...$names) => !empty(array_intersect(array_map('strtolower', $allFeats), array_map('strtolower', $names)));

        $features = [
            'garages'         => (float) ($property->garages ?? 0),
            'garden'          => $hasFeature('Garden', 'Landscaped', 'Garden Services') || $countSpaces('Garden') > 0,
            'pool'            => $hasFeature('Pool', 'Communal Pool', 'Indoor Pool', 'Splash Pool') || $countSpaces('Pool') > 0,
            'flatlet'         => $hasFeature('Flatlet') || $countSpaces('Flatlet') > 0,
            'petsAllowed'     => $hasFeature('Pet Friendly', 'Pets Allowed') ? 'Yes' : ($hasFeature('Pets Not Allowed') ? 'No' : 'DontKnow'),
            'furnishedStatus' => $hasFeature('Furnished') ? 'Yes' : ($hasFeature('Unfurnished') ? 'No' : 'No'),
        ];

        if ($property->beds) $features['bedrooms'] = (float) $property->beds;

        // Bathrooms. P24 has no separate half-bath field — BathroomsInfo.bathrooms
        // is a single float, and a half bathroom (guest toilet / cloakroom) is
        // represented as 0.5 of a bathroom. CoreX stores full baths (`baths`) and
        // half baths (`half_baths`, wizard scalar) as distinct additive counts and
        // renders the total everywhere as `baths + ½`, so the faithful P24 value is
        // baths + 0.5 per half bath (e.g. 2 full + 1 half → 2.5). Emit when EITHER
        // is set so a half-bath-only property still sends a bathroom count.
        $totalBaths = (float) ($property->baths ?? 0) + 0.5 * (int) ($property->half_baths ?? 0);
        if ($totalBaths > 0) $features['bathrooms'] = ['bathrooms' => $totalBaths];

        // Parking details from spaces/features. Amenity booleans use $hasAny —
        // parking amenities are entered on the Parking/Garage space, which
        // globalFeatures() strips (AT-146: property 6049 "Visitors Parking").
        $parkingSpaces = $countSpaces('Parking');
        if ($parkingSpaces > 0 || $hasAny('Carport', 'Secure Parking', 'Street Parking', 'Underground Parking', 'Visitors Parking', 'Shade Net Covered Parking', 'Double Parking', 'Single Parking', 'Tandem Parking', 'Triple Parking')) {
            $features['parking'] = [
                'parkingSpaces'        => $parkingSpaces > 0 ? (int) $parkingSpaces : null,
                'carport'              => $hasAny('Carport'),
                'secureParking'        => $hasAny('Secure Parking'),
                'onStreetParking'      => $hasAny('Street Parking'),
                'undergroundParking'   => $hasAny('Underground Parking'),
                'visitorsParking'      => $hasAny('Visitors Parking'),
                'shadeNetCoveredParking' => $hasAny('Shade Net Covered Parking'),
                'doubleParking'        => $hasAny('Double Parking'),
                'singleParking'        => $hasAny('Single Parking'),
                'tandemParking'        => $hasAny('Tandem Parking'),
                'tripleParking'        => $hasAny('Triple Parking'),
            ];
        }

        // Studies / offices
        $studies = $countSpaces('Study') + $countSpaces('Office');
        if ($studies > 0) $features['studies'] = (int) $studies;

        // Reception rooms — ONLY explicit "Reception Room" spaces. Lounge,
        // Dining Room and TV Room are deliberately NOT aggregated here: each
        // already surfaces as its own NAMED room via featureTags (Lounge /
        // DiningRoom / FamilyTVRoom), so also counting them as "reception rooms"
        // double-represented them and produced a "Reception Rooms N" the agent
        // never entered (property 6049: Lounge×1 + Dining×1 → phantom "Reception
        // Rooms 2"). Emitted UNCONDITIONALLY (incl. 0) so a re-push CLEARS a
        // stale count left by the old lounge+dining aggregation — P24 retains any
        // field absent from the payload (same reason internetAccess is always sent).
        $features['receptionRooms'] = (int) $countSpaces('Reception Room');

        // Domestic rooms
        $domestic = $countSpaces('Domestic Room');
        if ($domestic > 0) $features['domesticRooms'] = (int) $domestic;

        // Domestic bathrooms
        $domesticBaths = $countSpaces('Domestic Bathroom');
        if ($domesticBaths > 0) $features['domesticBathrooms'] = (float) $domesticBaths;

        // Outside toilets
        $outsideToilets = $countSpaces('Outside Toilet');
        if ($outsideToilets > 0) $features['outsideToilets'] = (int) $outsideToilets;

        // Second house / outbuildings
        $features['secondHouse'] = $countSpaces('Flatlet') > 0 || $countSpaces('Wendy House') > 0;

        // Standalone building
        if ($hasFeature('Standalone')) $features['hasStandaloneBuilding'] = true;

        // Wheelchair accessible
        if ($hasFeature('Wheelchair Friendly')) $features['isWheelchairAccessible'] = true;

        // Generator
        if ($hasFeature('Generator')) $features['hasGenerator'] = true;

        // Backup water
        if ($hasFeature('Backup Water', 'Water Tank', 'Borehole')) $features['hasBackupWater'] = true;

        // Internet access — ALWAYS sent so P24 reflects CoreX as the source of
        // truth. Only CoreX features with a true P24 InternetAccessInfo
        // equivalent are mapped; "Fast Internet" and "Wi-Fi" are generic flags
        // with NO P24 technology field, so they never set fibre/adsl/satellite
        // (inferring fibre from Fast Internet made listings show "Fibre
        // internet" on P24). The block is emitted unconditionally — including
        // all-false — so that a re-push deterministically CLEARS a stale
        // fibre/adsl/satellite a previous push set; omitting it lets P24 retain
        // the old value (P24 does not clear fields absent from the payload).
        $features['internetAccess'] = [
            'adsl'      => $hasFeature('ADSL'),
            'fibre'     => $hasFeature('Fibre'),
            'satellite' => $hasFeature('Satellite Internet', 'Satellite Dish'),
        ];

        // Sustainability
        if ($hasFeature('Solar Panel', 'Solar Geyser', 'Gas Geyser', 'Water Tank', 'Borehole', 'Backup Battery', 'Inverter')) {
            $features['sustainabilityInfo'] = [
                'solarPanels'             => $hasFeature('Solar Panel', 'Solar Heating'),
                'solarGeyser'             => $hasFeature('Solar Geyser'),
                'gasGeyser'               => $hasFeature('Gas Geyser'),
                'waterTank'               => $hasFeature('Water Tank'),
                'borehole'                => $hasFeature('Borehole'),
                'backupBatteryOrInverter' => $hasFeature('Backup Battery', 'Inverter'),
            ];
        }

        // Kitchens — emit when there's a counted Kitchen space OR a global kitchen
        // amenity that has a KitchensInfo field (so Coffee Machine / Sink /
        // Dishwasher route to PropertyFeatures, not loose tags).
        // Kitchen fittings live on the Kitchen space → $hasAny (globalFeatures strips them).
        $kitchens = $countSpaces('Kitchen');
        if ($kitchens > 0 || $hasAny('Dishwasher', 'Coffee Machine', 'Sink')) {
            $features['kitchens'] = [
                'kitchens'      => $kitchens > 0 ? (int) $kitchens : null,
                'dishwasher'    => $hasAny('Dishwasher'),
                'coffeeMachine' => $hasAny('Coffee Machine'),
                'sink'          => $hasAny('Sink'),
            ];
        }

        // Outside areas (balcony, courtyard, patio, veranda, etc.) — entered on
        // their space or a room → $hasAny (globalFeatures strips space/room copies).
        $patios = $countSpaces('Patio') + $countSpaces('Veranda') + $countSpaces('Lapa') + $countSpaces('Courtyard');
        if ($patios > 0 || $hasAny('Balcony', 'Courtyard')) {
            $features['outsideArea'] = [
                'outsideAreas' => (int) max($patios, 1),
                'balcony'      => $hasAny('Balcony'),
                'courtyard'    => $hasAny('Courtyard') || $countSpaces('Courtyard') > 0,
                'roofArea'     => false,
            ];
        }

        // Number of floors
        if ($hasFeature('Single Storey')) $features['numberOfFloors'] = 1;

        // Public transport
        if ($hasFeature('Near Bus Service', 'Near Train Service')) {
            $features['publicTransport'] = [
                'nearbyBusService'         => $hasFeature('Near Bus Service'),
                'nearbyMinibusTaxiService' => false,
                'nearbyTrainService'       => $hasFeature('Near Train Service'),
            ];
        }

        return $features;
    }

