    /**
     * Top-level P24 `tags[]` per the contract (.ai/specs/p24-featuretags-contract.md):
     * GLOBAL features that map to a P24 Tag enum member AND have NO
     * PropertyFeatures field. Amenities WITH a PropertyFeatures field are routed
     * there by buildPropertyFeatures() (External/Building/Other Features) and are
     * excluded here so they never appear as a loose tag. Per-room features are NOT
     * here either — they ride their room's featureTags[] entry. Case-insensitive
     * match against FEATURE_TAG_MAP; deduped; valid Tag enum members only.
     */
    private function buildTags(Property $property): array
    {
        $feats = $this->globalFeatures($property);
        if (empty($feats)) {
            return [];
        }

        // Lower-cased lookup so a stored 'sea view' still resolves to 'Sea View'.
        $lookup = [];
        foreach (self::FEATURE_TAG_MAP as $label => $tag) {
            $lookup[strtolower($label)] = $tag;
        }
        $propertyFeatureLabels = array_flip(self::PROPERTY_FEATURE_LABELS);
        $roomDetailOnly = array_flip(self::ROOM_DETAIL_ONLY_TAGS);

        $tags = [];
        foreach ($feats as $feat) {
            $key = strtolower(trim((string) $feat));
            // Amenities with a PropertyFeatures field are routed there, not here.
            if (isset($propertyFeatureLabels[$key])) {
                continue;
            }
            if (isset($lookup[$key])) {
                $tag = $lookup[$key];
                // Room-detail connectivity ports (TVPort/InternetPort/TelephonePort)
                // have NO valid listing-level home — at top level P24 renders them
                // as phantom rooms. They ride a room's featureTags[].tags only.
                if (isset($roomDetailOnly[$tag])) {
                    continue;
                }
                $tags[] = $tag;
            }
        }

        return array_values(array_unique($tags));
    }

    /**
     * AT-103 — the GLOBAL-ONLY feature set for tags[] + property-level booleans.
     *
     * global = features_json MINUS room-ONLY features, where a room-ONLY feature is one
     * that appears on a room (spaces['spaces'][].units[].features or a space's
     * featuresAll) AND is NOT in the explicit property feature-screen selection
     * (spaces['features']). A feature set BOTH globally and on a room STAYS global
     * (decision A — it also appears in featureTags[]).
     *
     * NB we do NOT use spaces['features'] as the sole source: it is empty/partial for
     * a large share of properties (imports + legacy edits store global features only in
     * the flat features_json with no per-room provenance). Subtracting only the
     * room-attributable features keeps those genuinely-global features intact (no
     * tags[] regression) while still stripping per-room-only leaks.
     *
     * @return string[]
     */
    private function globalFeatures(Property $property): array
    {
        $flat = array_values(array_unique(array_filter(
            array_map('strval', (array) ($property->features_json ?? [])),
            fn ($v) => trim($v) !== ''
        )));

        $sj = $property->spaces_json;
        $spaces = is_array($sj) ? ($sj['spaces'] ?? (isset($sj[0]) ? $sj : [])) : [];
        if (empty($spaces)) {
            return $flat; // legacy / no structured rooms — features_json is the global set
        }

        // Explicit property-screen global selection (may be empty for imports/legacy).
        $explicitGlobal = [];
        foreach (($sj['features'] ?? []) as $catArr) {
            $vals = is_array($catArr) ? $catArr : [$catArr];
            foreach ($vals as $f) {
                if (filled($f)) $explicitGlobal[strtolower(trim((string) $f))] = true;
            }
        }

        // Features attributable to a room (per-unit + space-level).
        $roomFeatures = [];
        foreach ($spaces as $sp) {
            foreach (($sp['featuresAll'] ?? []) as $f) {
                if (filled($f)) $roomFeatures[strtolower(trim((string) $f))] = true;
            }
            foreach (($sp['units'] ?? []) as $u) {
                foreach (($u['features'] ?? []) as $f) {
                    if (filled($f)) $roomFeatures[strtolower(trim((string) $f))] = true;
                }
            }
        }

        return array_values(array_filter($flat, function ($f) use ($roomFeatures, $explicitGlobal) {
            $k = strtolower(trim((string) $f));
            $roomOnly = isset($roomFeatures[$k]) && !isset($explicitGlobal[$k]);
            return !$roomOnly; // keep global + both-set; drop room-only
        }));
    }

    /**
     * AT-146 — the COMPLETE feature set a property carries: the flat features_json
     * UNION every space-level featuresAll and every per-unit feature. Unlike
     * globalFeatures() this does NOT strip room/space-attributable features, so it
     * is the correct source for "does the property have amenity X anywhere"
     * PropertyFeatures booleans (parking / kitchen fittings / outside areas) that
     * are entered on a dedicated space and would otherwise be invisible. Never
     * used for top-level tags[] (that stays globalFeatures() to avoid phantom rooms).
     *
     * @return string[]
     */
    private function allFeatures(Property $property): array
    {
        $all = array_map('strval', (array) ($property->features_json ?? []));

        $sj = $property->spaces_json;
        $spaces = is_array($sj) ? ($sj['spaces'] ?? (isset($sj[0]) ? $sj : [])) : [];
        foreach ($spaces as $sp) {
            foreach (($sp['featuresAll'] ?? []) as $f) {
                $all[] = (string) $f;
            }
            foreach (($sp['units'] ?? []) as $u) {
                foreach (($u['features'] ?? []) as $f) {
                    $all[] = (string) $f;
                }
            }
        }

        return array_values(array_unique(array_filter($all, fn ($v) => trim($v) !== '')));
    }

    /**
     * AT-102/AT-103 — build P24 featureTags[] so each room shows as a separate NAMED
     * room with its per-room features attached (the display agents expect). When a space
     * carries per-unit features, one entry per unit (Bedroom 1/2/3…) is emitted, each
     * carrying that unit's own features UNION the space-level featuresAll; when it does
     * not, one entry carries the space-level featuresAll. Both feature sources are mapped
     * via FEATURE_TAG_MAP. Unmapped feature labels and unmapped CoreX space types are
     * skipped and logged — never guessed. receptionRooms count is left untouched.
     *
     * @return array<int, array<string, mixed>>
     */
    private function buildFeatureTags(Property $property): array
    {
        $sj = $property->spaces_json;
        $spaces = is_array($sj) ? ($sj['spaces'] ?? (isset($sj[0]) ? $sj : [])) : [];

        $tagLookup = [];
        foreach (self::FEATURE_TAG_MAP as $label => $tag) {
            $tagLookup[strtolower($label)] = $tag;
        }

        $skippedTags = [];
        $mapRoomFeatures = function (array $features) use ($tagLookup, &$skippedTags): array {
            $tags = [];
            foreach ($features as $f) {
                $key = strtolower(trim((string) $f));
                if ($key === '') continue;
                if (isset($tagLookup[$key])) {
                    $tags[] = $tagLookup[$key];
                } else {
                    $skippedTags[] = (string) $f;
                }
            }
            return array_values(array_unique($tags));
        };

        $featureTags = [];
        $skippedSpaceTypes = [];

        foreach ($spaces as $sp) {
            $type = $sp['type'] ?? null;
            if (!$type) {
                continue;
            }
            $featureType = self::SPACE_TYPE_TO_P24_FEATURETYPE[$type] ?? null;
            if ($featureType === null) {
                $skippedSpaceTypes[] = (string) $type;
                continue;
            }

            // A space carries features in two places: space-level `featuresAll`
            // (apply to the whole space / every unit) and per-unit `features`
            // (specific to one unit). The room's feature set is the UNION.
            $spaceFeatures = $sp['featuresAll'] ?? [];
            $units         = $sp['units'] ?? [];
            $anyUnitFeatures = false;
            foreach ($units as $u) {
                if (!empty($u['features'] ?? [])) { $anyUnitFeatures = true; break; }
            }

            if ($anyUnitFeatures) {
                // Per-unit detail present → one named room per unit, each carrying
                // its OWN features PLUS the space-level featuresAll.
                foreach ($units as $u) {
                    $tags = $mapRoomFeatures(array_merge($spaceFeatures, $u['features'] ?? []));
                    // A room with no mapped features is pure noise on P24
                    // ("Bedroom 1 → Bedroom 1"). Emit an entry ONLY when the room
                    // actually carries >=1 feature. The room count itself is already
                    // conveyed via propertyInfo (bedrooms/bathrooms/etc.).
                    if (empty($tags)) {
                        continue;
                    }
                    // P24 labels the row from featureType ("Bedroom"). The unit
                    // label ("Bedroom 1") is the ROOM NAME — putting it in
                    // description duplicates the row label. Per the contract
                    // description is feature-prose only, and units carry no prose
                    // field, so description is omitted here.
                    $featureTags[] = ['featureType' => $featureType, 'tags' => $tags];
                }
            } else {
                // No per-unit detail anywhere — either the space has no units, or
                // every unit is empty and the agent entered the features at the
                // SPACE level (featuresAll). Emit ONE room from featuresAll.
                //
                // AT-146 (property 6049): this branch previously ran ONLY when a
                // space had literally zero units, so a space with an auto-created
                // empty unit (Kitchen/Lounge/Garage/Parking) silently DROPPED its
                // entire featuresAll set — the agent's Kitchen (Oven & Hob, Pantry,
                // Extractor Fan, …), Lounge and Garage features never reached P24.
                $tags = $mapRoomFeatures($spaceFeatures);
                if (empty($tags)) {
                    continue; // no features = no row
                }
                $entry = ['featureType' => $featureType, 'tags' => $tags];
                // description = genuine free-text/condition prose ONLY (never the
                // room name). descriptionAll is the space-level prose field.
                $desc = trim((string) ($sp['descriptionAll'] ?? ''));
                if ($desc !== '') {
                    $entry['description'] = $desc;
                }
                $featureTags[] = $entry;
            }
        }

        // NOTE: global amenities are NOT emitted as featureTags here. Those with a
        // PropertyFeatures field go to buildPropertyFeatures() (External/Building/
        // Other Features); those without go to top-level tags[] via buildTags().
        // featureType "Other" is never sent — P24 renders it as a junk room row.

        if (!empty($skippedTags) || !empty($skippedSpaceTypes)) {
            // Logging is best-effort — it must never break the mapping (and the logger
            // isn't booted in pure unit contexts).
            try {
                \Illuminate\Support\Facades\Log::channel('property24')->info(
                    'P24 featureTags — skipped unmapped items',
                    [
                        'property_id'         => $property->id,
                        'skipped_tags'        => array_values(array_unique($skippedTags)),
                        'skipped_space_types' => array_values(array_unique($skippedSpaceTypes)),
                    ]
                );
            } catch (\Throwable $e) {
                // no-op — never block syndication on a log write
            }
        }

        return $featureTags;
    }

    public function validate(array $payload): array
    {
        $errors = [];
        if (empty($payload['agencyId'])) $errors[] = 'No Property24 agency ID resolved — set it on the agency or branch.';
        // AT-P24 remediation (#3): P24 rejects a listing with no agents
        // ("A listing must have one or more agents"). Fail locally with an
        // actionable reason instead of letting the empty array reach P24. An
        // empty contactAgentIds means the listing agent has no P24 profile under
        // this agency yet (or the /agents lookup timed out) — register/sync the
        // agent to P24, then re-syndicate.
        if (empty($payload['contactAgentIds'])) {
            $errors[] = 'No Property24 agent linked — the listing agent is not registered on Property24 under this agency (or the agent lookup failed). Sync the agent to P24, then re-syndicate.';
        }
        if (empty($payload['description'])) $errors[] = 'Description is required';
        if (empty($payload['propertyInfo']['suburbId'])) $errors[] = 'Suburb unmapped — manual P24 suburb mapping required. Pick a Property24-recognised suburb on the property (Province → City → Suburb), then re-syndicate.';
        if (empty($payload['propertyInfo']['propertyTypeId'])) $errors[] = 'Property type could not be mapped to a P24 type ID';
        return $errors;
    }

    public function checkReadiness(Property $property): array
    {
        $missing = [];
        if (empty($property->description)) $missing[] = ['field' => 'description', 'label' => 'Description'];
        if (empty($property->suburb)) {
            $missing[] = ['field' => 'suburb', 'label' => 'Suburb'];
        } else {
            if (!$this->resolveSuburbId($property)) {
                $missing[] = ['field' => 'suburb_id', 'label' => 'Suburb not mapped to P24 ID (set in P24 Suburb Settings)'];
            }
        }
        if (empty($property->property_type)) $missing[] = ['field' => 'property_type', 'label' => 'Property Type'];
        if ($property->effectivePrice() <= 0 && !$property->price_on_application) $missing[] = ['field' => 'price', 'label' => 'Price (or enable Price On Application)'];
        if (empty($property->allImages())) $missing[] = ['field' => 'images', 'label' => 'At least one photo'];
        if (empty($property->listing_type) && empty($property->mandate_type)) $missing[] = ['field' => 'listing_type', 'label' => 'Listing Type (Sale/Rental)'];
        if (empty($property->resolveP24AgencyId())) {
            $missing[] = [
                'field' => 'p24_agency_id',
                'label' => 'Property24 agency ID not configured on branch or agency',
            ];
        }
        return $missing;
    }

    /**
     * Resolve the P24 agency ID for a property. Branch override > agency default.
     * Throws when neither is set so submission fails loudly rather than
     * silently routing to the wrong profile (or the wrong tenant).
     */
    private function resolveP24AgencyId(Property $property): int
    {
        $resolved = $property->resolveP24AgencyId();
        if ($resolved === null || $resolved === '') {
            $branchName = $property->branch?->name ?? '(no branch)';
            $agencyName = $property->agency?->name ?? $property->branch?->agency?->name ?? '(no agency)';
            throw new Property24ConfigurationException(
                "Property #{$property->id} cannot be syndicated: no Property24 agency ID on "
                . "branch '{$branchName}' or agency '{$agencyName}'."
            );
        }
        return (int) $resolved;
    }

    private function buildPhotos(Property $property): array
    {
        $photos = [];
        $catData = $property->gallery_categories_json;

        // Build a map of image URL → category name for captions
        $captionMap = [];
        if ($catData && isset($catData['categories'])) {
            foreach ($catData['categories'] as $cat) {
                foreach ($cat['images'] ?? [] as $img) {
                    $captionMap[$img] = $cat['name'];
                }
            }
        }

        // AT-101: per-agency photo cap. Source = syndicationImages() (the exact
        // gallery the agent curates and sees, gallery_images_json), NOT allImages()
        // — allImages() additionally merges images_json (a divergent public mirror)
        // and the dawn/noon/dusk sets, over-counting photos the user never put in
        // the gallery (e.g. property #1322: gallery 45 vs allImages 68). What goes
        // to P24 must equal the CoreX gallery.
        $maxPhotos = $property->agency?->p24MaxPhotos() ?? Agency::P24_DEFAULT_MAX_PHOTOS;
        $allImages = $property->syndicationImages();

        // AT-P24 remediation (#7): never silently truncate. If the curated
        // gallery exceeds the agency photo cap, log a warning naming the
        // dropped count so an over-cap listing is visible (e.g. property 3361:
        // 160 → 150, 10 dropped) rather than quietly losing photos.
        if (count($allImages) > $maxPhotos) {
            try {
                Log::channel('property24')->warning(
                    'P24 photo cap exceeded — photos dropped from submission',
                    [
                        'property_id' => $property->id,
                        'gallery'     => count($allImages),
                        'cap'         => $maxPhotos,
                        'dropped'     => count($allImages) - $maxPhotos,
                    ]
                );
            } catch (\Throwable $e) {
                // never block syndication on a log write
            }
        }

        $images = array_slice($allImages, 0, $maxPhotos);

        // Read the raw bytes for every resolvable image first (one disk read
        // each) so the payload can be size-checked before base64 encoding.
        $raw = [];
        foreach ($images as $imagePath) {
            if (empty($imagePath)) continue;

            $diskPath = $this->urlToDiskPath($imagePath);

            if ($diskPath && Storage::disk('public')->exists($diskPath)) {
                $bytes = Storage::disk('public')->get($diskPath);
                if (empty($bytes)) continue;

                $raw[] = [
                    'bytes'   => $bytes,
                    'mime'    => Storage::disk('public')->mimeType($diskPath) ?: 'image/jpeg',
                    'caption' => $captionMap[$imagePath] ?? null,
                ];
            }
        }

        // P24 hard-caps a submit at 60MB of image data. If the set overflows
        // the budget, downscale the oversized images to their fair share so the
        // whole gallery fits — never drop a photo, never fail the submit.
        $raw = $this->fitPhotosToPayloadBudget($raw, $property);

        foreach ($raw as $entry) {
            $photos[] = [
                'bytes'           => base64_encode($entry['bytes']),
                'mimeContentType' => $entry['mime'],
                'caption'         => $entry['caption'],
                'isFloorPlan'     => false,
            ];
        }

        return $photos;
    }

    /**
     * Keep a property's combined photo payload under the P24 60MB cap. Budgets
     * on the base64-encoded size (what is actually transmitted); when the set
     * already fits, returns it untouched (the common case — no re-encoding, full
     * quality). When it overflows, gives every photo an equal share of the
     * budget and re-encodes only those over their share, so all photos survive.
     *
     * @param array<int,array{bytes:string,mime:string,caption:?string}> $raw
     * @return array<int,array{bytes:string,mime:string,caption:?string}>
     */
    private function fitPhotosToPayloadBudget(array $raw, Property $property): array
    {
        if (empty($raw)) {
            return $raw;
        }

        // Exact standard-base64 encoded length of a raw byte string.
        $b64Len = static fn(string $b): int => intdiv(strlen($b) + 2, 3) * 4;

        $total = array_sum(array_map(fn($e) => $b64Len($e['bytes']), $raw));
        if ($total <= self::P24_IMAGE_PAYLOAD_BUDGET_BYTES) {
            return $raw;
        }

        // Fair per-image share of the budget (in base64 bytes), converted to a
        // raw-byte target the GD encoder packs to.
        $perImageB64 = intdiv(self::P24_IMAGE_PAYLOAD_BUDGET_BYTES, count($raw));
        $rawTarget   = intdiv($perImageB64 * 3, 4);
        $compressed  = 0;

        foreach ($raw as $i => $entry) {
            if ($b64Len($entry['bytes']) <= $perImageB64) {
                continue; // already within its share — leave at full quality
            }

            $shrunk = $this->compressImageToBytes($entry['bytes'], $rawTarget);
            if ($shrunk !== null) {
                $raw[$i]['bytes'] = $shrunk;
                $raw[$i]['mime']  = 'image/jpeg';
                $compressed++;
            }
        }

        $after = array_sum(array_map(fn($e) => $b64Len($e['bytes']), $raw));

        try {
            Log::channel('property24')->warning(
                'P24 image payload exceeded 60MB cap — photos downscaled to fit',
                [
                    'property_id'      => $property->id,
                    'photos'           => count($raw),
                    'compressed'       => $compressed,
                    'before_b64_bytes' => $total,
                    'after_b64_bytes'  => $after,
                    'budget_bytes'     => self::P24_IMAGE_PAYLOAD_BUDGET_BYTES,
                ]
            );
        } catch (\Throwable $e) {
            // never block syndication on a log write
        }

        return $raw;
    }

    /**
     * Re-encode an image via GD to at most $maxBytes (raw JPEG). Steps quality
     * down at full resolution first, then progressively downscales, until it
     * fits. Returns the smallest result achieved (guaranteed under budget for
     * any real photo); returns null only when GD cannot decode the source, in
     * which case the caller keeps the original bytes.
     */
    private function compressImageToBytes(string $bytes, int $maxBytes): ?string
    {
        if ($maxBytes < 1 || ! function_exists('imagecreatefromstring')) {
            return null;
        }

        $src = @imagecreatefromstring($bytes);
        if (! $src instanceof \GdImage) {
            return null;
        }

        $w = imagesx($src);
        $h = imagesy($src);
        $best = null;

        foreach ([1.0, 0.8, 0.6, 0.45, 0.33, 0.25] as $scale) {
            $tw = max(1, (int) round($w * $scale));
            $th = max(1, (int) round($h * $scale));

            $dst = ($scale === 1.0) ? $src : imagecreatetruecolor($tw, $th);
            if ($dst !== $src) {
                imagecopyresampled($dst, $src, 0, 0, 0, 0, $tw, $th, $w, $h);
            }

            for ($q = 80; $q >= 40; $q -= 10) {
                ob_start();
                imagejpeg($dst, null, $q);
                $out = (string) ob_get_clean();
                $best = $out; // each step is smaller than the last

                if (strlen($out) <= $maxBytes) {
                    if ($dst !== $src) imagedestroy($dst);
                    imagedestroy($src);
                    return $out;
                }
            }

            if ($dst !== $src) imagedestroy($dst);
        }

        imagedestroy($src);
        return $best; // smallest achievable — still far below the original
    }

    /**
     * Convert a Storage::url() path back to a disk-relative path.
     * e.g. "/storage/properties/16/file.jpg" => "properties/16/file.jpg"
     * or "https://domain.com/storage/properties/16/file.jpg" => "properties/16/file.jpg"
     */
    private function urlToDiskPath(string $url): ?string
    {
        // Strip domain if full URL
        if (str_starts_with($url, 'http://') || str_starts_with($url, 'https://')) {
            $parsed = parse_url($url);
            $url = $parsed['path'] ?? $url;
        }

        // Strip the /storage/ prefix that Storage::url() adds
        if (str_contains($url, '/storage/')) {
            return substr($url, strpos($url, '/storage/') + 9); // 9 = strlen('/storage/')
        }

        // If it's already a relative path like "properties/16/file.jpg"
        if (str_starts_with($url, 'properties/')) {
            return $url;
        }

        return null;
    }

    /**
     * Resolve the P24 numeric suburb ID (p24_suburbs.p24_id) to send in the
     * payload. Order: trust the stored, chain-verified FK; then a name match
     * that is DISAMBIGUATED BY CITY/PROVINCE and only ever accepted when it
     * resolves to exactly ONE suburb. We never guess between same-named
     * suburbs in different cities — returning null makes the caller skip
     * syndication and flag the property for manual mapping (AT-104).
     *
     * Background (AT-104): "Glenmore" exists in P24 in both Durban (id 10787)
     * and Port Edward / South Coast (id 10790). The previous resolver checked
     * `pp_suburb_id` (a Private Property column that is NULL on every CoreX
     * property, so the branch never fired) and then fell through to a bare
     * `P24Suburb::lookup()->first()` with no city filter — sending the Durban
     * Glenmore for every South Coast Glenmore. This rewrite kills that class
     * of bug for all 30+ multi-city collision suburb names, not just Glenmore.
     */
    private function resolveSuburbId(Property $property): ?int
    {
        // TIER 1 — trust the stored, chain-verified P24 suburb FK. The property
        // create/edit form and the wizard write `p24_suburb_id` only after
        // AppliesP24Location has verified the suburb→city→province chain against
        // P24's own hierarchy, so it is the authoritative mapping. NEVER fall
        // through to name guessing when it is set — that is the bug we are fixing.
        //
        // AT-P24 remediation (#2): the p24_id is only returned when its
        // p24_suburbs row is P24-VERIFIED (p24_verified_at set by the daily
        // p24:sync-locations stamp-and-sweep). An unverified row carries a
        // p24_id that P24's listing API may reject ("Value [N] for property
        // listing.SuburbId is invalid" — e.g. Addington 5997), so we never send
        // it. Soft-deleted rows are already excluded by the SoftDeletes scope.
        if ($property->p24_suburb_id) {
            $suburb = P24Suburb::find($property->p24_suburb_id);
            if ($suburb && $suburb->p24_id && $suburb->p24_verified_at !== null) {
                return (int) $suburb->p24_id;
            }
        }

        if (!$property->suburb) return null;

        // TIER 2 — exact name match, disambiguated by city/province, unique-only.
        $exact = $this->resolveUniqueLocalSuburb($property, false);
        if ($exact && $exact->p24_id) return (int) $exact->p24_id;

        // TIER 3 — fuzzy name match (handles trailing "Beach", punctuation, extra
        //          whitespace, …), same city/province constraint, same unique rule.
        $fuzzy = $this->resolveUniqueLocalSuburb($property, true);
        if ($fuzzy && $fuzzy->p24_id) return (int) $fuzzy->p24_id;

        // TIER 4 — no unique, city-qualified match. Do NOT guess and do NOT fall
        // back to a name-only API lookup (it would re-introduce the wrong-city
        // bug). Return null so submitListing()'s validate() flags the property
        // ("Suburb unmapped — manual P24 suburb mapping required") and skips it.
        return null;
    }

    /**
     * Resolve the property's suburb to exactly ONE P24Suburb row, disambiguated
     * by city/province. Returns null when zero — or MORE THAN ONE — candidate
     * survives, so we never guess between same-named suburbs in different
     * cities. $fuzzy widens the name match from exact to LIKE.
     */
    private function resolveUniqueLocalSuburb(Property $property, bool $fuzzy): ?P24Suburb
    {
        $candidates = $this->suburbNameCandidates((string) $property->suburb, $fuzzy);
        if ($candidates->isEmpty()) return null;
        if ($candidates->count() === 1) return $candidates->first();

        // Multiple same-named suburbs across cities — keep only those in a city
        // this property could legitimately belong to.
        $cityIds = $this->candidateCityIds($property);
        if (empty($cityIds)) return null; // no city signal → cannot disambiguate

        $filtered = $candidates->whereIn('p24_city_id', $cityIds)->values();
        return $filtered->count() === 1 ? $filtered->first() : null;
    }

    /**
     * Candidate P24Suburb rows whose name matches the given suburb name.
     * Exact mode matches slug or case-insensitive name equality; fuzzy mode
     * widens to a normalised LIKE both ways.
     *
     * @return \Illuminate\Support\Collection<int, P24Suburb>
     */
    private function suburbNameCandidates(string $suburbName, bool $fuzzy): \Illuminate\Support\Collection
    {
        $key = strtolower(trim($suburbName));
        if ($key === '') return collect();

        // AT-P24 remediation (#2): only P24-VERIFIED rows are eligible candidates.
        // An unverified row must never be returned (its p24_id may be rejected by
        // P24) nor count toward the uniqueness test that disambiguates collisions.
        if (!$fuzzy) {
            $slug = str_replace(' ', '-', $key);
            return P24Suburb::whereNotNull('p24_verified_at')
                ->where(function ($q) use ($slug, $key) {
                    $q->where('slug', $slug)->orWhereRaw('LOWER(name) = ?', [$key]);
                })
                ->get();
        }

        $normalised = strtolower(preg_replace('/[^a-z0-9 ]+/i', '', trim($suburbName)));
        if ($normalised === '') return collect();

        return P24Suburb::whereNotNull('p24_verified_at')
            ->where(function ($q) use ($normalised) {
                $q->whereRaw('LOWER(name) LIKE ?', ['%' . $normalised . '%'])
                  ->orWhereRaw('? LIKE CONCAT(\'%\', LOWER(name), \'%\')', [$normalised]);
            })
            ->get();
    }

    /**
     * The set of P24 city IDs (p24_cities.id) this property could legitimately
     * sit in, used to disambiguate collision suburb names. Trust order: the
     * stored p24_city_id FK first, else the city/town text (province-constrained
     * when the province is known). Empty array = no usable city signal.
     *
     * @return int[]
     */
    private function candidateCityIds(Property $property): array
    {
        if ($property->p24_city_id) {
            return [(int) $property->p24_city_id];
        }

        $cityNames = array_values(array_unique(array_filter([
            strtolower(trim((string) $property->city)),
            strtolower(trim((string) $property->town)),
        ], fn ($v) => $v !== '')));
        if (empty($cityNames)) return [];

        $query = P24City::query()->where(function ($q) use ($cityNames) {
            foreach ($cityNames as $n) {
                $q->orWhereRaw('LOWER(name) = ?', [$n]);
            }
        });

        // Province narrows same-named cities to the right one when we know it.
        $provinceId = $this->resolveProvinceId($property);
        if ($provinceId) {
            $query->where('p24_province_id', $provinceId);
        }

        return $query->pluck('id')->map(fn ($id) => (int) $id)->all();
    }

    /**
     * Resolve the property's province to a p24_provinces.id. Trust the stored
     * FK first; else match the province text after normalising both sides
     * (P24 stores "KwaZulu Natal", CoreX often holds "KwaZulu-Natal"/"KZN").
     */
    private function resolveProvinceId(Property $property): ?int
    {
        if ($property->p24_province_id) return (int) $property->p24_province_id;

        $name = $this->normaliseProvince($property->province);
        if ($name === '') return null;

        $key = preg_replace('/[^a-z0-9]+/i', '', strtolower($name));
        foreach (P24Province::all() as $province) {
            if (preg_replace('/[^a-z0-9]+/i', '', strtolower((string) $province->name)) === $key) {
                return (int) $province->id;
            }
        }
        return null;
    }

    private function normaliseProvince(?string $province): string
    {
        $p = strtolower(trim((string) $province));
        if ($p === '') return '';
        return match (true) {
            str_contains($p, 'kwazulu') || $p === 'kzn'   => 'KwaZulu-Natal',
            str_contains($p, 'western')                   => 'Western Cape',
            str_contains($p, 'eastern')                   => 'Eastern Cape',
            str_contains($p, 'northern')                  => 'Northern Cape',
            str_contains($p, 'north west')                => 'North West',
            str_contains($p, 'gauteng') || $p === 'gp'    => 'Gauteng',
            str_contains($p, 'mpumalanga')                => 'Mpumalanga',
            str_contains($p, 'limpopo')                   => 'Limpopo',
            str_contains($p, 'free state')                => 'Free State',
            default                                        => ucwords($p),
        };
    }

    /**
     * Map CoreX property type to P24 propertyTypeId.
     * IDs sourced from GET /listing/v53/property-types on ExDev:
     *   4 = House, 5 = Apartment/Flat, 6 = Townhouse,
     *   8 = Vacant Land/Plot, 10 = Farm, 11 = Commercial, 12 = Industrial
     */
    private function resolvePropertyTypeId(?string $type): ?int
    {
        if (empty($type)) return null;

        // Normalise: lowercase, replace any non-alphanum with single space, collapse, trim.
        // Lets us match "Apartment / Flat", "Apartment/Flat", "apartment-flat", etc.
        $norm = trim(preg_replace('/\s+/', ' ', preg_replace('/[^a-z0-9]+/i', ' ', strtolower($type))));

        $padded = " {$norm} ";
        $contains = function (string ...$needles) use ($padded): bool {
            foreach ($needles as $n) {
                if (str_contains($padded, " {$n} ")) return true;
            }
            return false;
        };

        if ($contains('industrial')) return 12;
        if ($contains('commercial', 'office', 'retail', 'hospitality')) return 11;
        if ($contains('farm', 'smallholding', 'small holding', 'agricultural')) return 10;
        if ($contains('vacant land', 'land', 'plot', 'stand', 'erf')) return 8;
        if ($contains('townhouse', 'duplex', 'simplex', 'cluster')) return 6;
        if ($contains('apartment', 'flat', 'penthouse')) return 5;
        if ($contains('house', 'freestanding', 'free standing', 'cottage', 'garden cottage')) return 4;

        return 4; // default: House
    }

    private function resolveContactAgentIds(Property $property, int $agencyId): array
    {
        // Build an agency-scoped client so credentials come from the property's
        // agency row (not the now-empty .env). Without this the client falls
        // back to env creds, authenticates as nobody, and returns zero agents
        // — which P24 then rejects as "must have one or more agents".
        $agency = $property->agency ?? \App\Models\Agency::find($property->agency_id);
        $client = new Property24ApiClient($agency);
        // Scope the lookup to the property's resolved agency — otherwise a
        // property under agency B would pull agents from agency A's feed.
        $result = $client->getAgents((string) $agencyId);

        if (!$result['success']) return [];

        $agents = $result['data'] ?? [];
        $ids = [];

        // Agents who opted out of P24 must never be attached to a syndicated
        // listing, even if a stale P24 record still carries their sourceReference.
        $excludedUserIds = \App\Models\User::withTrashed()
            ->whereIn('id', array_filter([$property->agent_id, $property->pp_second_agent_id]))
            ->where('exclude_from_p24', true)
            ->pluck('id')
            ->all();

        // Primary agent
        if ($property->agent_id && !in_array((int) $property->agent_id, $excludedUserIds, true)) {
            $sourceRef = 'CoreX-Agent-' . $property->agent_id;
            foreach ($agents as $agent) {
                if (($agent['sourceReference'] ?? '') === $sourceRef) {
                    $ids[] = (int) $agent['id'];
                    break;
                }
            }
        }

        // Second agent
        if ($property->pp_second_agent_id && !in_array((int) $property->pp_second_agent_id, $excludedUserIds, true)) {
            $sourceRef = 'CoreX-Agent-' . $property->pp_second_agent_id;
            foreach ($agents as $agent) {
                if (($agent['sourceReference'] ?? '') === $sourceRef) {
                    $ids[] = (int) $agent['id'];
                    break;
                }
            }
        }

        return $ids;
    }

    private function buildShowdays(Property $property): array
    {
        $showdays = [];
        $active = $property->activeShowdays ?? $property->showdays()->where('active', true)->where('end_date', '>=', now())->get();

        foreach ($active as $s) {
            $showdays[] = [
                'startDate' => $s->start_date->format('Y-m-d\TH:i:s'),
                'endDate'   => $s->end_date->format('Y-m-d\TH:i:s'),
            ];
        }

        return $showdays;
    }

    /**
     * Map CoreX property status to P24 ListingStatus enum.
     * P24 statuses: NewListing, Active, Rented, Withdrawn, BackOnMarket,
     *               Expired, Extended, RaisedPrice, ReducedPrice,
     *               Cancelled, Pending, Sold, CancelledSale
     */
    private function mapPropertyStatus(Property $property): string
    {
        return self::getP24Status($property->status, $property->p24_ref, $property->status_label);
    }

    /**
     * Static helper: convert a CoreX two-tier status (base status + optional
     * sub-label banner) to a P24 ListingStatus. Used by both the mapper and the
     * observer.
     *
     * Two-tier model (mirrors P24/Propcon): a listing has a BASE status and an
     * optional SUB-LABEL on an on-market base — e.g. For Sale + "Reduced Price",
     * For Sale + "Pending", For Sale + "Back on Market". Every sub-label banner
     * is an ON-MARKET refinement, so it may only refine an on-market base: a
     * TERMINAL / off-market base status (sold, withdrawn, expired, cancelled,
     * let-out, draft, …) is resolved FIRST and is absolute — it can never be
     * overridden by a banner. Only once the base is on-market does the sub-label
     * take precedence; an on-market base with no label falls through to the
     * base-status logic.
     *
     * WHY the terminal base wins (the bug this ordering fixes): a listing is
     * routinely Active + a sticky banner (e.g. "Reduced Price"). When it is
     * later withdrawn/sold, the banner is not always cleared alongside the base
     * status (the web edit form clears it; other ingress paths historically did
     * not). If the banner were still resolved first, getP24Status() would return
     * 'ReducedPrice' — an on-market state — and the observer would leave the
     * listing LIVE on the portal even though CoreX shows it Withdrawn. A stale
     * on-market banner must never resurrect an off-market listing.
     *
     * Back-compat: $statusLabel is optional and defaults to null. With a null
     * label this returns exactly what the prior flat-status version returned for
     * every base status — proven by the AT-P24 before/after round-trip table —
     * with one intentional fix: a let-out rental now maps to 'Rented' instead of
     * silently falling through to 'NewListing'. The only other behaviour change
     * is the intended one above: a terminal base + on-market banner now resolves
     * to the terminal status instead of the banner.
     */
    public static function getP24Status(?string $corexStatus, ?string $p24Ref = null, ?string $statusLabel = null): string
    {
        $normalise = static function (?string $v): string {
            $v = strtolower(str_replace(['•', '_'], ['', ' '], trim($v ?? '')));
            return preg_replace('/\s+/', ' ', $v); // collapse multiple spaces
        };

        $status = $normalise($corexStatus);

        // 1) A terminal / off-market BASE status is absolute. It is resolved
        //    before the sub-label so a stale on-market banner (Reduced Price,
        //    Back on Market, …) can never keep a withdrawn/sold/expired listing
        //    live on the portal. See the WHY note in the docblock.
        $terminal = match (true) {
            str_contains($status, 'sold')              => 'Sold',
            str_contains($status, 'rented')
                || str_contains($status, 'let out')     => 'Rented',
            str_contains($status, 'withdrawn')
                || str_contains($status, 'unavailable') => 'Withdrawn',
            str_contains($status, 'expired')            => 'Expired',
            str_contains($status, 'cancelled')
                || str_contains($status, 'archived')    => 'Cancelled',
            str_contains($status, 'draft')              => 'Withdrawn',
            default                                     => null,
        };
        if ($terminal !== null) {
            return $terminal;
        }

        // 2) On-market base: the sub-label (banner) takes precedence — it is the
        //    authoritative P24 lifecycle refinement on top of an Active listing.
        $label = $normalise($statusLabel);
        if ($label !== '') {
            $fromLabel = match (true) {
                str_contains($label, 'reduced')        => 'ReducedPrice',
                str_contains($label, 'raised')         => 'RaisedPrice',
                str_contains($label, 'back on market') => 'BackOnMarket',
                str_contains($label, 'under offer')
                    || str_contains($label, 'pending') => 'Pending',
                default                                => null,
            };
            if ($fromLabel !== null) {
                return $fromLabel;
            }
        }

        // 3) On-market base status (no resolving label).
        return match (true) {
            str_contains($status, 'under offer')
                || str_contains($status, 'pending')     => 'Pending',
            str_contains($status, 'back on market')     => 'BackOnMarket',
            str_contains($status, 'reduced')            => 'ReducedPrice',
            str_contains($status, 'raised')             => 'RaisedPrice',
            str_contains($status, 'auction')            => 'Active',
            $p24Ref !== null                            => 'Active',
            default                                     => 'NewListing',
        };
    }

    /**
     * Check if a P24 status is a terminal/off-market status.
     */
    public static function isTerminalStatus(string $p24Status): bool
    {
        return in_array($p24Status, ['Sold', 'Rented', 'Withdrawn', 'Expired', 'Cancelled']);
    }

    /**
     * Does pushing this P24 status take the listing OFF the portal?
     *
     * Terminal is NOT the same as removed. 'Sold' and 'Rented' are terminal
     * market states that P24 keeps on the portal (it shows them as sold/rented
     * stock); only 'Withdrawn', 'Expired' and 'Cancelled' actually remove the
     * listing. Conflating the two stranded live listings: a Sold push wrote
     * p24_syndication_status='deactivated', and every delist path downstream
     * then skipped the property as "already off the portal" while P24's own
     * is-on-portal still answered true.
     *
     * Audit: .ai/audits/p24-sold-not-delisted-2026-07-10.md (property #2142)
     */
    public static function removesFromPortal(string $p24Status): bool
    {
        return in_array($p24Status, ['Withdrawn', 'Expired', 'Cancelled'], true);
    }

    private function mapListingType(?string $type): string
    {
        if (empty($type)) return 'Sale';
        return match (strtolower($type)) {
            'sale', 'for sale', 'sell' => 'Sale',
            'rental', 'rent', 'to let' => 'Rental',
            default => 'Sale',
        };
    }

    private function parseStreetName(?string $address): string
    {
        if (empty($address)) return '';
        $parts = explode(',', $address);
        return preg_replace('/^\d+\s+/', '', trim($parts[0] ?? ''));
    }

    private function extractYouTubeId(?string $input): ?string
    {
        if (!$input) return null;
        $input = trim($input);
        if (preg_match('/^[a-zA-Z0-9_-]{11}$/', $input)) return $input;
        if (preg_match('/(?:youtube\.com\/(?:watch\?\S*?v=|embed\/|shorts\/|live\/|v\/)|youtu\.be\/|v=)([a-zA-Z0-9_-]{11})/', $input, $m)) {
            return $m[1];
        }
        return null;
    }
